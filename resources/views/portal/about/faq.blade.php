 <h4 class="is-600 mb-5">Preguntas Frecuentes</h4>
  <h6 class="is-600 mb-3">¿Qué es un Plan de Metas?</h5>
  <p class="is-700">El intendente o gobernador tiene que decir concretamente que hará, cómo lo hará y cuáles deben ser los logros mínimos para cada año.</p>
  <p>Es una ordenanza que establece que los gobiernos municipales deben detallar objetivos, actividades, metas claras y medibles para la acción de gobierno.</p>
  <p>El <b>Plan de Metas</b> es un instrumento de <b>planificación, gestión, información pública, rendición de cuentas y seguimiento</b> de las acciones a desarrollar por un gobierno durante el período que dura un mandato.</p>
  <p>El Plan de Metas, sancionado por una Ordenanza municipal, establece la obligatoriedad de que los intendentes, al iniciar una gestión, deben fijar objetivos en el marco de un esquema de trabajo, que abarque a las diversas áreas temáticas- Estas metas deben cumplirse durante su gobierno e es parte del proceso el informar periódicamente a la ciudadanía sobre el estado de las mismas.</p>
  <h6 class="is-600 mb-3">¿Cómo funciona?</h5>
  <p>El Gobierno Local  define e informa los proyectos del Plan de Metas y realiza <b>reportes</b> periódicos informando el estado y los avances de estas políticas.
Hay cuatro estados para los proyectos: <b>inactivos, en progreso, demorados y finalizados.</b></p>
  <h6 class="is-600 mb-3">¿Que proporciona “SUMEN” a los ciudadanos de las ciudades de Mendoza?</h5>
  <p>El objetivo de “Sumen” es ayudar a las personas a informarse, monitorear y participar respecto de la ejecución de las metas con las que un gobierno se compromete.</p>
  <ul>
    <li>Conocer las metas y proyectos</li>
    <li>Corroborar su estado, filtrado por distrito y temática</li>
    <li>Monitorear y recibir información actualizada</li>
    <li>Validar y comentar sobre reportes</li>
  </ul>
  <p>Esta herramienta <b>permite conocer la opinión de los ciudadanos respecto a acciones concreta</b>; lo que se convierte en insumo para que los procesos de evaluación, ajuste y reformulación de Planes de Metas sean más inclusivos.
</p>
<p>Creemos que una Planificación inclusiva permite una mejor identificación de los problemas y de las posibles soluciones, lo que potencia una gestión más eficiente.</p>
  <h6 class="is-600 mb-3">¿Por qué usar Sumen en tu gobierno?</h5>
  <p>Esta herramienta permite a las gestiones municipales hacer público, visible y amigable su <b>Plan de Metas de Gobierno</b>. 
Es un nuevo canal de comunicación con los ciudadanos; priorizando la transparencia y el acceso a la información para, entre todos, colaborar en la construcción de lo Público.
</p>
<p>A través de <b>Sumen</b> las gestiones de gobierno pueden difundir las acciones que llevan adelante y su grado de progreso de forma fácil y dinámica.
</p>
  <h6 class="is-600 mb-3">¿ Por qué es necesario Sumen?</h5>
  <p>De esta manera se promueve una ciudadanía activa, y una mejor relación entre gobierno y sociedad. Estas prácticas sirven para mejorar e incidir las políticas públicas y construyen un Estado más transparente.</p>
