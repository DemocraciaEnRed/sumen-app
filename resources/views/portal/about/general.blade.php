<section id="quienes-somos" class="py-4">
<h4 class="is-600 mb-4">¿Qué es Nuestra Mendoza?</h4>
<p><b>Nuestra Mendoza</b> es una iniciativa local, fundada en el año 2009 por El Diario Los Andes, AVINA y VALOS.</p>
<p>Esta Fundación integra la Red Argentina de Ciudades Justas, Democráticas y Sustentables y la Red Latinoamericana por Ciudades Justas, Democráticas y Sustentables;y articula con más de 70 iniciativas de diferentes lugares de América Latina..</p>
<p>Nuestra Mendoza se define como un espacio no partidario que promueve:
<ul>
<li>La participación ciudadana en la construcción y monitoreo de las Políticas Públicas</li> 
<li>La transparencia en las Gestiones de Gobierno.</li>
</p>
</ul>
<p>Esta Fundación busca contribuir al desarrollo de una <b>Mendoza más justa</b>, con igualdad de oportunidades, democrática, con participación ciudadana en la toma de decisiones, y sostenible, que pueda satisfacer necesidades del presente sin comprometer la supervivencia de las generaciones futuras.</p>
</section>
